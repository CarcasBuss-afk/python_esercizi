""" SCOPO del programma: costruire un indirizzo email aggiungendo parti con += """

# Crea una variabile email con il valore "mario"
email = "mario"

# Aggiungi "@" alla variabile email
email += "@"

# Aggiungi "esempio" alla variabile email
email += "esmpio"

# Aggiungi ".com" alla variabile email
email += ".com"

# Stampa la variabile email
print(email)

