# Riepilogo operatore +=

""" SCOPO: Crea una variabile 'frase' con il valore "Python".
Aggiungi progressivamente usando l'operatore += le seguenti parti:
- " è"
- " un linguaggio"
- " fantastico"
Stampa la frase completa. """
python = "Python"
python += " è"
python += " un linguaggio"
python += " fantastico"
print(python)
""" Output atteso:
Python è un linguaggio fantastico
"""
