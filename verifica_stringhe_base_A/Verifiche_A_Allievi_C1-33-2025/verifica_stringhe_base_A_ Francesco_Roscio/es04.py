""" SCOPO del programma: aggiungere parti a un messaggio usando l'operatore += """

# Crea una variabile messaggio con il valore "Buongiorno"
messaggio = "Buongiorno"

# Aggiungi " a tutti" alla variabile messaggio
messaggio  += " a tutti"

# Aggiungi "!" alla variabile messaggio
messaggio += "!"

# Stampa il messaggio
print(messaggio)

""" Output atteso:
Buongiorno a tutti!
"""
