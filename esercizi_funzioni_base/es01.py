""" SCOPO: impariamo a creare e chiamare una funzione base senza parametri.
Una funzione è un blocco di codice riutilizzabile che esegue un compito specifico. """

# COSA SONO LE FUNZIONI?
# Le funzioni permettono di organizzare il codice in blocchi riutilizzabili.
# Invece di scrivere lo stesso codice più volte, lo mettiamo in una funzione
# e la chiamiamo quando serve.
#
# SINTASSI BASE:
# def nome_funzione():
#     # codice da eseguire
#     istruzioni...
#
# - 'def' = parola chiave per definire una funzione
# - 'nome_funzione' = nome che diamo alla funzione (usa snake_case)
# - '():' = parentesi tonde (vuote se non ci sono parametri) + due punti
# - Codice indentato = corpo della funzione (con 4 spazi)
#
# CHIAMARE UNA FUNZIONE:
# nome_funzione()  # usa le parentesi per eseguire la funzione

# Definiamo la prima funzione: saluta
def saluta():
    print("Ciao! Benvenuto nel programma!")
    print("Questa è la mia prima funzione!")

# Ora chiamiamo la funzione saluta
saluta()

print("\n--- Separatore ---\n")

# Possiamo chiamare la stessa funzione più volte
saluta()
