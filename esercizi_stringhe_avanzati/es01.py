""" SCOPO: impariamo a estrarre porzioni di una stringa usando lo slicing.
Estraiamo i primi 5 caratteri, i caratteri dalla posizione 3 alla 8, gli ultimi 4 caratteri. """

# Lo SLICING permette di estrarre una porzione (sottostringhe) di una stringa
# Sintassi: stringa[inizio:fine]
# - inizio: posizione da cui iniziare (inclusa)
# - fine: posizione dove fermarsi (ESCLUSA)
# Esempio: "Python"[0:3] restituisce "Pyt" (caratteri in posizione 0, 1, 2)

# Forme speciali:
# stringa[:n] = primi n caratteri (da inizio fino a posizione n)
# stringa[n:] = da posizione n fino alla fine
# stringa[-n:] = ultimi n caratteri

parola = "Programmazione"

# Estraiamo i primi 5 caratteri
primi_cinque = parola[0:5]
print(f"Primi 5 caratteri: {primi_cinque}")

# Estraiamo i caratteri dalla posizione 3 alla posizione 8 (esclusa)
# Posizioni: 3, 4, 5, 6, 7
