# SLICING CON STEP POSITIVO
# Sintassi: stringa[inizio:fine:step]
# Lo step indica "ogni quanti caratteri" prenderne uno
#
# stringa[::2] = prende un carattere ogni 2, partendo dalla posizione 0 (posizioni pari: 0, 2, 4, 6...)
# stringa[1::2] = prende un carattere ogni 2, partendo dalla posizione 1 (posizioni dispari: 1, 3, 5, 7...)
#
# Esempio: "Python"[::2] = "Pto" (caratteri in posizione 0, 2, 4)
# Esempio: "Python"[1::2] = "yhn" (caratteri in posizione 1, 3, 5)

