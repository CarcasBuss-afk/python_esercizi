""" SCOPO del programma: costruire un URL aggiungendo parti con += """

# Crea una variabile url con il valore "http://"
url = "http://"

# Aggiungi "www." alla variabile url
url += "www."

# Aggiungi "negozio" alla variabile url
url += "negozio"

# Aggiungi ".it" alla variabile url
url += "it"

# Stampa l'URL
print(url)

""" Output atteso:
http://www.negozio.it
"""
